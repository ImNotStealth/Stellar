	sf::Texture& AssetManager::GetTexture(const std::filesystem::path& _filePath)
	{
		if (!Get().textureMap.contains(_filePath))
		{
			sf::Texture texture;
			if (texture.loadFromFile("Assets/" + _filePath.string()))
			{
				std::cout << "Loaded Texture: " << _filePath << std::endl;
				Get().textureMap.emplace(_filePath, texture);
			}
            else
            {
				Get().textureMap.emplace(_filePath, Get().textureError);
            }
		}
		
		return Get().textureMap.at(_filePath);
	}

	sf::Font& AssetManager::GetFont(const std::filesystem::path& _filePath)
	{
		if (!Get().fontMap.contains(_filePath))
		{
			std::shared_ptr<sf::Font> font = std::make_shared<sf::Font>();
			if (font->loadFromFile("Assets/" + _filePath.string()))
			{
				std::cout << "Loaded Font: " << _filePath << std::endl;
				Get().fontMap[_filePath] = font;
			}
			else
			{
				throw std::runtime_error("Failed to load Font: " + _filePath.string());
			}
		}
		return *Get().fontMap.at(_filePath);
	}

	sf::SoundBuffer& AssetManager::GetSoundBuffer(const std::filesystem::path& _filePath)
	{
		if (!Get().soundBufferMap.contains(_filePath))
		{
			sf::SoundBuffer buffer;
			if (buffer.loadFromFile("Assets/" + _filePath.string()))
			{
				std::cout << "Loaded SoundBuffer: " << _filePath << std::endl;
			}
			Get().soundBufferMap.emplace(_filePath, std::move(buffer));
		}

		return Get().soundBufferMap.at(_filePath);
	}

	sf::Music& AssetManager::GetMusic(const std::filesystem::path& _filePath)
	{
		if (!Get().musicMap.contains(_filePath))
		{
			auto music = std::make_unique<sf::Music>();
			if (music->openFromFile("Assets/" + _filePath.string()))
			{
				Get().musicMap.emplace(_filePath, std::move(music));
				std::cout << "Loaded Music: " << _filePath << std::endl;
			}
		}

		return *Get().musicMap.at(_filePath);
	}

	AssetManager& AssetManager::Get()
	{
		if (!instance)
		{
			instance = new AssetManager();
            instance->textureError.loadFromMemory(textureErrorData, 64 * 64 * sizeof(unsigned char));
		}

		return *instance;
	}

	void AssetManager::Shutdown()
	{
		if (instance)
		{
			delete instance;
			instance = nullptr;
		}
	}
